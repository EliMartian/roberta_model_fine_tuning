      loss.backward()
      optim.step()

      if (batch_idx + 1) % 20 == 0:  # Print progress every 20 batches
          print(f"Epoch [{epoch + 1}/{num_train_epochs}], Batch [{batch_idx + 1}/{len(train_loader)}], Loss: {total_loss / (batch_idx + 1):.4f}")

